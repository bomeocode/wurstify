<h2 class="mb-4">So wirst du zum Wurst-Influencer! 🌭</h2>

<p class="lead">Du hast eine legendäre Bratwurst entdeckt und willst sie mit der Welt teilen? Perfekt! Mit dieser Anleitung wirst du in Rekordzeit zum offiziellen Wurst-Insider.</p>

<hr class="my-4">

<dl class="row">
  <dt class="col-sm-3">Schritt 1: Der Ruf der Wurst</dt>
  <dd class="col-sm-9">Alles beginnt mit dem großen, gelben <strong>Plus-Button</strong> auf der Karte. Ein Klick darauf ist dein erster Schritt zum Ruhm.</dd>

  <dt class="col-sm-3">Schritt 2: Wo ist der Grill?</dt>
  <dd class="col-sm-9">Im Bewertungsfenster, das sich öffnet, klickst du auf <strong>"Meinen aktuellen Standort verwenden"</strong>. Unsere App ortet dich und schaut nach, welche bekannten Grill-Meister sich in deiner Nähe befinden. Alternativ kannst du auch einfach eine Postleitzahl oder Adresse eintippen.</dd>

  <dt class="col-sm-3">Schritt 3: Bekannt oder unbekannt?</dt>
  <dd class="col-sm-9">Die App zeigt dir jetzt eine Liste mit Vorschlägen.
    <ul class="list-unstyled mt-2">
      <li>✅ <strong>Ist dein Anbieter dabei?</strong> Super! Klicke einfach auf den Namen.</li>
      <li>🆕 <strong>Ist dein Anbieter ein Juwel?</strong> Kein Problem, weiter zu Schritt 4!</li>
    </ul>
  </dd>

  <dt class="col-sm-3">Schritt 4: Werde zum Entdecker!</dt>
  <dd class="col-sm-9">Wenn der Anbieter neu ist, tippe einfach seinen Namen (z.B. "Gerd's Grillbude am See") und die Adresse so genau wie möglich selbst ein. Damit fügst du einen neuen Hotspot zur Wurst-Karte hinzu!</dd>

  <dt class="col-sm-3">Schritt 5: Das Urteil!</dt>
  <dd class="col-sm-9">Jetzt kommt der wichtigste Teil: <strong>Deine Bewertung!</strong> Vergib Sterne, lade optional bis zu drei Fotos hoch und hinterlasse einen Kommentar für die Nachwelt. Klick auf "Speichern" – fertig!</dd>
</dl>

<div class="alert alert-success mt-4">
  Das war's schon! So einfach leistest du einen heldenhaften Beitrag zur deutschen Bratwurst-Kultur. Gut gemacht, Wurst-Influencer! 🏆
</div>